// original code:
//   http://rosettacode.org/wiki/Ray-casting_algorithm#Go

func TectonicPlate(lat, lon float64) string {
	for k, v := range tectonicPlates {
		var inside bool
		if len(v) > 1 {
			for i := 1; i < len(v); i++ {
				if intersectsPlate(tectonicPoint{lon, lat}, v[i-1], v[i]) {
					inside = !inside
				}
			}
		}
		if inside {
			return k
		}
	}
	return "Unknown Tectonic Plate"
}

func intersectsPlate(point, start, end tectonicPoint) bool {
	// handle negative point longitudes ...
	for point.lon < 0.0 {
		point.lon += 360.0
	}
	// handle the longitude jump ...
	if math.Abs(end.lon-start.lon) > 180.0 {
		if start.lon > end.lon {
			start.lon -= 360.0
		} else {
			start.lon += 360.0
		}
	}
	// swap points in needed ...
	if start.lon > end.lon {
		start, end = end, start
	}

	// need to be outside the polygon ...
	for point.lon == start.lon || point.lon == end.lon {
		point = tectonicPoint{math.Nextafter(point.lon, math.Inf(1)), point.lat}
	}

	// check ends
	if point.lon < start.lon || point.lon > end.lon {
		return false
	}

	// check between points
	if start.lat > end.lat {
		if point.lat > start.lat {
			return false
		}
		if point.lat < end.lat {
			return true
		}
	} else {
		if point.lat > end.lat {
			return false
		}
		if point.lat < start.lat {
			return true
		}
	}

	// check line between the two points
	return (point.lon-start.lon)/(point.lat-start.lat) >= (end.lon-start.lon)/(end.lat-start.lat)
}
